    private void btncambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncambiarActionPerformed
        if(txtactual.getText().isEmpty() || txtnueva.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "¡No puedes dejar campos en blanco!", "Atención", JOptionPane.CANCEL_OPTION);
        }else if(!((txtnueva.getText()).equals(txtconfirmar.getText()))){
            JOptionPane.showMessageDialog(this, "¡Las nuevas contraseñas no coinciden!", "Atención", JOptionPane.CANCEL_OPTION);
        }else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT id_admin FROM administradores WHERE id_admin = ? AND contra_admin = md5(sha1(?))");
                st.setInt(1, ClsHelp.ProCodigoPer);
                st.setString(2, txtactual.getText());
                ResultSet rs = st.executeQuery();
                if(rs.next())
                {
                    st = cn.prepareStatement("UPDATE administradores SET contra_admin = md5(sha1(?)) WHERE id_admin = ?");
                    st.setString(1, txtnueva.getText());
                    st.setInt(2, ClsHelp.ProCodigoPer);
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "¡Contraseña modificada con exito!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
                else {
                        JOptionPane.showMessageDialog(this, "¡Datos incorrectos!", "ERROR", JOptionPane.CANCEL_OPTION);
                        txtactual.setText(null);
                        txtnueva.setText(null);
                        txtconfirmar.setText(null);
                        txtactual.requestFocusInWindow();
                    }
            } catch (SQLException | HeadlessException e) {JOptionPane.showMessageDialog(this, "ERROR: " + e, "Error", JOptionPane.ERROR_MESSAGE);}
        }
    }//GEN-LAST:event_btncambiarActionPerformed

