    private void MostrarDatos(int nombre) {
        modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        modelo.addColumn("Código");
        modelo.addColumn("Fecha");
        modelo.addColumn("Salida");
        modelo.addColumn("Devolvió");
        modelo.addColumn("Vendió");
        modelo.addColumn("Se esperaba");
        modelo.addColumn("Entregó");
        modelo.addColumn("Comisión");
        modelo.addColumn("Sueldo");
        modelo.addColumn("Gasolina");
        modelo.addColumn("Saldo Neto");
        modelo.addColumn("Recibido por");
        modelo.addColumn("Comentario");
        tablaruta.setModel(modelo);
        String sql = "SELECT id_ruta, fecha_ruta, cant_salida_ruta, devol_ruta, vendidos_ruta, cantidad_esp_ruta,"
                + "entregado_ruta, ganancia_vend_ruta, sueldo_vend_ruta, gas_ruta, saldo_neto_ruta, nom_admin, coment_ruta"
                + " FROM rutas r INNER JOIN empleados e INNER JOIN administradores a ON r.id_emp=e.id_emp AND r.id_admin=a.id_admin "
                + "WHERE e.id_emp = " + nombre + " ORDER BY fecha_ruta ASC";
        String[] datos = new String[13];
        try {
            Statement st2 = cn.createStatement();
            ResultSet rs = st2.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);
                datos[9] = rs.getString(10);
                datos[10] = rs.getString(11);
                datos[11] = rs.getString(12);
                datos[12] = rs.getString(13);
                modelo.addRow(datos);
            }
            tablaruta.setModel(modelo);
            tablaruta.getTableHeader().setReorderingAllowed(false);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "ERROR: " + ex, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

