    private void txtsalidaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsalidaKeyReleased
        if ("".equals(txtsalida.getText()) || "".equals(txtdevueltos.getText())) {
            lblvendidos.setText("0");
            lblesperada.setText("0");
            lblsueldo.setText("0");
        } else {
            int vendidos = Integer.parseInt(txtsalida.getText()) - Integer.parseInt(txtdevueltos.getText());
            lblvendidos.setText(String.valueOf(vendidos));
            double esperado = Integer.parseInt(lblvendidos.getText()) * 0.83;
            lblesperada.setText(String.valueOf(df.format(esperado)));
            if(vendidos > 115){
                double sueldo = vendidos * 0.08;
                lblsueldo.setText(String.valueOf(df.format(sueldo)));
            }else{
                double sueldo = vendidos * 0.08;
                lblsueldo.setText(String.valueOf(df.format(sueldo)));
            }
            if(txtentregado.getText().isEmpty() || txtgas.getText().isEmpty()){}else{
                double comision = Double.parseDouble(txtentregado.getText()) - Double.parseDouble(lblesperada.getText());
                lblcomision.setText(String.valueOf(df.format(comision)));
                double neto = Double.parseDouble(txtentregado.getText()) - (Double.parseDouble(lblcomision.getText()) + Double.parseDouble(lblsueldo.getText()) + Integer.parseInt(txtgas.getText()));
                lblneto.setText(String.valueOf(df.format(neto)));
            }
        }
    }//GEN-LAST:event_txtsalidaKeyReleased

