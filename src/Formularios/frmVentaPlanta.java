    private void cmbtipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbtipoItemStateChanged
        switch (cmbtipo.getSelectedIndex()) {
            case 0:
                txttipo.setText("");
                txttipo.setEnabled(false);
                txtmonto.setEnabled(false);
                if(!(txtfardos.getText().isEmpty())){
                    double division = Double.parseDouble(txtfardos.getText()) / 3;
                    double monto = division * 2.5;
                    txtmonto.setText(String.valueOf(df.format(monto)));
                }   break;
            case 1:
                txttipo.setText("");
                txttipo.setEnabled(false);
                txtmonto.setEnabled(false);
                if(!(txtfardos.getText().isEmpty())){
                    double monto = Double.parseDouble(txtfardos.getText()) * 1.00;
                    txtmonto.setText(String.valueOf(df.format(monto)));
                }   break;
            case 2:
                txttipo.setText("");
                txttipo.setEnabled(false);
                txtmonto.setEnabled(false);
                if(!(txtfardos.getText().isEmpty())){
                    double monto = Double.parseDouble(txtfardos.getText()) * 0.70;
                    txtmonto.setText(String.valueOf(df.format(monto)));
                }   break;
            case 3:
                txttipo.setText("");
                txttipo.setEnabled(false);
                txtmonto.setEnabled(false);
                if(!(txtfardos.getText().isEmpty())){
                    double monto = Double.parseDouble(txtfardos.getText()) * 0.75;
                    txtmonto.setText(String.valueOf(df.format(monto)));
                }   break;
            case 4:
                txttipo.setText("");
                txttipo.setEnabled(false);
                txtmonto.setEnabled(false);
                if(!(txtfardos.getText().isEmpty())){
                    double monto = Double.parseDouble(txtfardos.getText()) * 0.65;
                    txtmonto.setText(String.valueOf(df.format(monto)));
                }   break;
            case 5:
                txttipo.setText("");
                txttipo.setEnabled(false);
                txtmonto.setEnabled(false);
                if(!(txtfardos.getText().isEmpty())){
                    double monto = Double.parseDouble(txtfardos.getText()) * 0.60;
                    txtmonto.setText(String.valueOf(df.format(monto)));
                }   break;
            case 6:
                txttipo.setEnabled(true);
                txtmonto.setEnabled(true);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_cmbtipoItemStateChanged

