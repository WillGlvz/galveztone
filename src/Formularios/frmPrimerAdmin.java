    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(txtName.getText().isEmpty() || txtApe.getText().isEmpty() || txtNick.getText().isEmpty() || txtClave.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puedes dejar campos en blanco", "Oye...",JOptionPane.CANCEL_OPTION);
        }
        else{
            try {
                st = cn.prepareStatement("INSERT INTO administradores(nom_admin, ape_admin, usuario_admin, contra_admin) VALUES(?, ?, ?, md5(sha1(?)))");
                st.setString(1, txtName.getText());
                st.setString(2, txtApe.getText());
                st.setString(3, txtNick.getText());
                st.setString(4, txtClave.getText());
                st.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "!Excelente, JefeÂ¡ Podemos comenzar.");
                frmBanner obj = new frmBanner(null, true);
                dispose();
                obj.setVisible(true);
            } catch (SQLException | HeadlessException e) {JOptionPane.showMessageDialog(this, "ERROR: " + e, "Error", JOptionPane.ERROR_MESSAGE);}
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

