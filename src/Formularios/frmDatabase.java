    private void btnrespaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrespaldoActionPerformed
        try {
            int ArchivoAbierto;
            ArchivoAbierto = FileRuta.showOpenDialog(this);
            if (ArchivoAbierto == JFileChooser.APPROVE_OPTION) {
                File ArchivoSeleccionado = new File(FileRuta.getSelectedFile().getCanonicalPath() + "." + ((FileNameExtensionFilter) FileRuta.getFileFilter()).getExtensions()[0]);
                String ruta = ArchivoSeleccionado.getAbsolutePath();
                Process p = Runtime.getRuntime().exec("C:/Program Files/MariaDB 10.1/bin/mysqldump --opt --user=root --password=19720716 ventas");
                InputStream is = p.getInputStream();
                try (FileOutputStream fos = new FileOutputStream(ruta)) {
                    byte[] buffer = new byte[1000];
                    int leido = is.read(buffer);
                    while (leido > 0) {
                        fos.write(buffer, 0, leido);
                        leido = is.read(buffer);
                    }     
                    JOptionPane.showMessageDialog(this, "Respaldo de base de datos creado con éxito");
                }
            }else {
                JOptionPane.showMessageDialog(this, "¡No se creó el backup de la base de datos!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (HeadlessException | IOException e) {JOptionPane.showMessageDialog(this, "Error: " + e, "ERROR", JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_btnrespaldoActionPerformed

