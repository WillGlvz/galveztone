            final String APP_KEY = "kjx0ph5uxexjfyy";
            final String APP_SECRET = "iw6eb4tx5u5eupq";
            DbxAppInfo appInfo = new DbxAppInfo(APP_KEY, APP_SECRET);
            DbxRequestConfig config = new DbxRequestConfig(
                "Respaldo_DropBox", Locale.getDefault().toString());
            DbxWebAuthNoRedirect webAuth = new DbxWebAuthNoRedirect(config, appInfo);
            String authorizeUrl = webAuth.start();
            Desktop.getDesktop().browse(new URI(authorizeUrl));
            String code = JOptionPane.showInputDialog(this,"Copie el código que se le proporcionó",JOptionPane.QUESTION_MESSAGE);
            DbxAuthFinish authFinish = webAuth.finish(code);
            String accessToken = authFinish.accessToken;
            DbxClient client = new DbxClient(config, accessToken);
            String ruta = "C:/Users/Will Vásquez/Documents/backup.sql";
            Process p = Runtime.getRuntime().exec("C:/Program Files/MariaDB 10.1/bin/mysqldump --opt --user=root --password=19720716 ventas");
            InputStream is = p.getInputStream();
            FileOutputStream fos = new FileOutputStream(ruta);
            byte[] buffer = new byte[1000];
            int leido = is.read(buffer);
            while (leido > 0) {fos.write(buffer, 0, leido);leido = is.read(buffer);} fos.close();
            int dia = calendar.get(Calendar.DAY_OF_MONTH);
            int mes = calendar.get(Calendar.MONTH)+1;
            int anio = calendar.get(Calendar.YEAR);
            File inputFile = new File(ruta);
            FileInputStream inputStream = new FileInputStream(inputFile);
            DbxEntry.File uploadedFile = client.uploadFile("/DbGlaciar/backup_"+dia+"_"+mes+"_"+anio+".sql", DbxWriteMode.add(), inputFile.length(), inputStream);
